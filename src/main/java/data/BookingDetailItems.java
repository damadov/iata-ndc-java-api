package data;

public class BookingDetailItems {

	public Boolean Honeymoon = false;
	public Boolean Smoking = false;
	public Boolean KingBed = false;
	public Boolean LateArrival = false;
	
	public Boolean getHoneymoon() {
		return Honeymoon;
	}
	public void setHoneymoon(Boolean honeymoon) {
		Honeymoon = honeymoon;
	}
	public Boolean getSmoking() {
		return Smoking;
	}
	public void setSmoking(Boolean smoking) {
		Smoking = smoking;
	}
	public Boolean getKingBed() {
		return KingBed;
	}
	public void setKingBed(Boolean kingBed) {
		KingBed = kingBed;
	}
	public Boolean getLateArrival() {
		return LateArrival;
	}
	public void setLateArrival(Boolean lateArrival) {
		LateArrival = lateArrival;
	}
}
