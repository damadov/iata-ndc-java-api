package data;

public class Company {

	public String companyCode;
	public String companyName;
	public String description;
	public String vkno;
	public String vkloc;
	public String vktitle;
	public String address;
	public String city;
	public String country;
	public String email;
	public String phone;
	
	public String getCompanyCode() {
		return companyCode;
	}
	public void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getVkno() {
		return vkno;
	}
	public void setVkno(String vkno) {
		this.vkno = vkno;
	}
	public String getVkloc() {
		return vkloc;
	}
	public void setVkloc(String vkloc) {
		this.vkloc = vkloc;
	}
	public String getVktitle() {
		return vktitle;
	}
	public void setVktitle(String vktitle) {
		this.vktitle = vktitle;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}

	
}
