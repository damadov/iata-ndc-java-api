package data;

public class FlightRules {

	public String rule_url;
	public String rule_desc;
	public String rule_format;
	public int index;
	
	
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getRule_url() {
		return rule_url;
	}
	public void setRule_url(String rule_url) {
		this.rule_url = rule_url;
	}
	public String getRule_desc() {
		return rule_desc;
	}
	public void setRule_desc(String rule_desc) {
		this.rule_desc = rule_desc;
	}
	public String getRule_format() {
		return rule_format;
	}
	public void setRule_format(String rule_format) {
		this.rule_format = rule_format;
	}
	
	
}
