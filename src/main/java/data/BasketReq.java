package data;

public class BasketReq {

	public String custlist;
	public String basket_guid;
	public String currency;
	
	
	public String getCustlist() {
		return custlist;
	}
	public void setCustlist(String custlist) {
		this.custlist = custlist;
	}
	public String getBasket_guid() {
		return basket_guid;
	}
	public void setBasket_guid(String basket_guid) {
		this.basket_guid = basket_guid;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
	
}
